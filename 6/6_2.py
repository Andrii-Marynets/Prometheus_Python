def encode_morze(text):
    text = str(text)
    text = text.upper()
    morse = ''
    for i in text:
        if i == ' ':
            morse = morse.rstrip("___")
            morse += "_______"
            continue
        if morse_code.has_key(i) == False:
            morse += ''
            continue
        x = morse_code.get(i)
        for j in x:
            if j == '.':
                morse += "^"
            elif j == '-':
                morse += "^^^"
            morse += '_'
        morse = morse.rstrip('_')
        morse += "___"
    morse = morse.rstrip("___")
    return morse
